-- Миграция 0018: поставщик в РП становится необязательным.
--
-- Контекст:
--   В РП теперь можно включать заявки типов «Подрядчик работа (СМР)» и «Своя закупка»,
--   у которых поставщика может не быть. Ранее rp_letters.supplier_id был NOT NULL, что
--   не позволяло создавать РП без поставщика. Снимаем NOT NULL; внешний ключ на suppliers
--   остаётся (nullable FK допустим). Для РП без поставщика в реестре показывается прочерк.
--
-- Без top-level BEGIN/COMMIT — runner оборачивает миграцию в транзакцию (ADR-0002).
-- Повторный запуск безопасен: DROP NOT NULL идемпотентен по факту (колонка остаётся nullable).

ALTER TABLE public.rp_letters
  ALTER COLUMN supplier_id DROP NOT NULL;
